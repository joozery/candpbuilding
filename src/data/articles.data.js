/** Helper: หาบทความจาก id */
export const getArticleById = (id) => articles.find(a => String(a.id) === String(id))

/** Helper: บทความอื่นที่คล้ายกัน */
export const getRelatedArticles = (id, limit = 3) =>
    articles.filter(a => String(a.id) !== String(id)).slice(0, limit)

/** Helper: เฉพาะบทความที่เผยแพร่แล้ว */
export const getPublishedArticles = () => articles.filter(a => a.status === 'published')
